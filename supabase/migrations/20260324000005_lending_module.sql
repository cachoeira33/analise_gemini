-- =============================================================================
-- SECTION 6: RPC — generate_loan_installments
-- =============================================================================
-- Input:  p_loan_id UUID — must already exist in public.loans
-- Output: INT — number of installment rows inserted
--
-- Formula selection:
--   SIMPLE INTEREST
--     Total payable = P × (1 + r × n)
--     Each installment = ROUND(total_payable / n, 2)
--
--   COMPOUND INTEREST (French/Price amortisation — equal PMT)
--     When r > 0:
--       PMT = P × r × (1 + r)^n / ((1 + r)^n - 1)
--     When r = 0 (zero-interest plan):
--       PMT = P / n
--
-- Due dates: start_date + (installment_number × 30) days
--   e.g. start_date=2026-04-01, n=3 → due 2026-05-01, 2026-06-01, 2026-07-01
-- =============================================================================
CREATE OR REPLACE FUNCTION public.generate_loan_installments(p_loan_id UUID)
RETURNS INT
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
    v_loan              RECORD;
    v_pmt               NUMERIC(15, 2);
    v_power             NUMERIC;          -- (1 + r)^n  used twice in PMT
    v_installment_num   INT;
    v_inserted          INT := 0;
BEGIN
    -- ── 1. Fetch and lock the loan row ────────────────────────────────────────
    SELECT *
    INTO   v_loan
    FROM   public.loans
    WHERE  id = p_loan_id
    FOR UPDATE;                           -- prevent concurrent double-generation

    IF NOT FOUND THEN
        RAISE EXCEPTION 'Loan not found: %', p_loan_id;
    END IF;

    -- ── 2. Guard: do not regenerate if installments already exist ─────────────
    IF EXISTS (
        SELECT 1 FROM public.loan_installments WHERE loan_id = p_loan_id
    ) THEN
        RAISE EXCEPTION
            'Installments already generated for loan %. Delete existing rows first.', p_loan_id;
    END IF;

    -- ── 3. Calculate per-installment payment amount ────────────────────────────
    IF v_loan.interest_type = 'simple' THEN
        -- Simple interest: spread P×(1 + r×n) evenly
        v_pmt := ROUND(
            v_loan.principal_amount * (1 + v_loan.interest_rate * v_loan.total_installments)
            / v_loan.total_installments,
            2
        );

    ELSE
        -- Compound interest (PMT / anuidade / French amortisation)
        IF v_loan.interest_rate = 0 THEN
            -- Zero-rate edge case: simple division
            v_pmt := ROUND(v_loan.principal_amount / v_loan.total_installments, 2);
        ELSE
            v_power := POWER(
                (1 + v_loan.interest_rate)::NUMERIC,
                v_loan.total_installments::NUMERIC
            );
            v_pmt := ROUND(
                v_loan.principal_amount * v_loan.interest_rate * v_power / (v_power - 1),
                2
            );
        END IF;
    END IF;

    -- ── 4. Insert one row per installment ─────────────────────────────────────
    FOR v_installment_num IN 1 .. v_loan.total_installments LOOP
        INSERT INTO public.loan_installments (
            loan_id,
            installment_number,
            due_date,
            expected_amount,
            paid_amount,
            status
        ) VALUES (
            p_loan_id,
            v_installment_num,
            v_loan.start_date + (v_installment_num * 30),    -- 30-day intervals
            v_pmt,
            0,
            'pending'
        );
        v_inserted := v_inserted + 1;
    END LOOP;

    RETURN v_inserted;
END;
$$;

COMMENT ON FUNCTION public.generate_loan_installments(UUID) IS
    'Accepts a loan_id. Calculates the equal periodic payment (PMT for compound, '
    'simple-interest slice for simple type) and inserts loan_installments rows '
    '30 days apart from the loan start_date. Returns the count of rows inserted. '
    'Raises an exception if the loan does not exist or installments already exist.';

-- Grant to authenticated so it can be called as an RPC from the client
GRANT EXECUTE ON FUNCTION public.generate_loan_installments(UUID) TO authenticated;

