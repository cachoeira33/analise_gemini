-- ----------------------------------------------------------------------------
-- TABLE: loans (Contratos de Empréstimo)
-- One row per loan contract. Anchored to a business and optionally a CRM customer.
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.loans (
    id                  UUID            PRIMARY KEY DEFAULT gen_random_uuid(),

    -- Tenant isolation: every loan is owned by exactly one business.
    business_id         UUID            NOT NULL
                            REFERENCES public.businesses(id) ON DELETE CASCADE,

    -- Optional link to CRM customer. NULL = anonymous / external borrower.
    customer_id         UUID
                            REFERENCES public.customers(id) ON DELETE SET NULL,

    -- Financial terms
    principal_amount    NUMERIC(15, 2)  NOT NULL
                            CHECK (principal_amount > 0),
    interest_rate       NUMERIC(8, 6)   NOT NULL
                            CHECK (interest_rate >= 0),           -- decimal fraction: 0.02 = 2 %/period
    interest_type       public.interest_type NOT NULL,
    total_installments  INT             NOT NULL
                            CHECK (total_installments > 0),

    -- Contract lifecycle
    status              public.loan_status NOT NULL DEFAULT 'active',
    start_date          DATE            NOT NULL,                  -- reference for first due date
    created_at          TIMESTAMPTZ     NOT NULL DEFAULT NOW()
);

COMMENT ON TABLE public.loans IS
    'One row per loan contract. All repayment maths pivot on principal_amount, interest_rate, interest_type. '
    'Call generate_loan_installments(id) after INSERT to auto-create parcelas.';

COMMENT ON COLUMN public.loans.interest_rate IS
    'Decimal fraction per period. Example: 0.015 = 1.5 % per month. '
    'Compound interest uses this as the monthly rate in the PMT formula. '
    'Simple interest spreads P×(1 + r×n) evenly over n periods.';

COMMENT ON COLUMN public.loans.customer_id IS
    'Optional FK to public.customers. NULL means the borrower has no CRM profile.';

-- ----------------------------------------------------------------------------
-- TABLE: loan_installments (Parcelas)
-- One row per scheduled repayment. Generated by the RPC; never inserted directly.
-- ----------------------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.loan_installments (
    id                  UUID            PRIMARY KEY DEFAULT gen_random_uuid(),

    loan_id             UUID            NOT NULL
                            REFERENCES public.loans(id) ON DELETE CASCADE,

    installment_number  INT             NOT NULL
                            CHECK (installment_number > 0),
    due_date            DATE            NOT NULL,

    -- Expected repayment amount (PMT or simple slice) — set once at generation time.
    expected_amount     NUMERIC(15, 2)  NOT NULL
                            CHECK (expected_amount > 0),

    -- Actual amount received. Defaults to 0, supports partial payments (baixa dinâmica).
    -- When paid_amount >= expected_amount the row is considered fully paid.
    paid_amount         NUMERIC(15, 2)  NOT NULL DEFAULT 0
                            CHECK (paid_amount >= 0),

    status              public.installment_status NOT NULL DEFAULT 'pending',

    -- Set by the application when status transitions to 'paid'
    paid_at             TIMESTAMPTZ,

    CONSTRAINT uq_loan_installment UNIQUE (loan_id, installment_number)
);

COMMENT ON TABLE public.loan_installments IS
    'Scheduled repayment rows (parcelas). Auto-generated via generate_loan_installments(). '
    'Do not INSERT manually — use the RPC to ensure consistency with the loan terms.';

COMMENT ON COLUMN public.loan_installments.paid_amount IS
    'Supports baixa dinâmica (partial payments). '
    'Application logic should set status=''partial'' when 0 < paid_amount < expected_amount, '
    'and status=''paid'' + paid_at=now() when paid_amount >= expected_amount.';

